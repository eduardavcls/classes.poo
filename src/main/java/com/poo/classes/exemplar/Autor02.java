public class Autor02 {
private String autor;
private int id;
 private String nome;
 

public Autor02(String nome) {
    this.nome = nome;
}   


public String getAutor() {
    return autor;
}
public void setAutor(String autor) {
    this.autor = autor;
}
public int getId() {
    return id;
}
public void setId(int id) {
    this.id = id;
}
public String getNome() {
    return nome;
}
public void setNome(String nome) {
    this.nome = nome;
}
    
}
