public class Editora02 {
private String editora;
private int id;
private String nome;

//construtor
public Editora02(String nome) {
    this.nome = nome;
}

//getters and setters
public String getEditora() {
    return editora;
}
public void setEditora(String editora) {
    this.editora = editora;
}
public int getId() {
    return id;
}
public void setId(int id) {
    this.id = id;
}
public String getNome() {
    return nome;
}
public void setNome(String nome) {
    this.nome = nome;
}


}
